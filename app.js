genBtn.addEventListener("click", onBtnClick);

function onBtnClick(event) {
  event.preventDefault();
  printBox.innerHTML = "";
  const nameCount = isNumValid(inputNum.value);
  printName(nameCount);
  inputNum.value = "";
}

function isNumValid(num) {
  if (isNaN(num) || num < 1) {
    return 1;
  } else if (num > 10) {
    return 10;
  } else {
    return num;
  }
}

function printName(namecount) {
  for (let i = 0; i < namecount; i++) {
    const name = genName();
    const newName = document.createElement("span");
    newName.innerText = name;
    printBox.appendChild(newName);
  }
}

function genName() {
  const ranNum1 = Math.floor(Math.random() * firstName.length);
  const ranNum2 = Math.floor(Math.random() * lastName.length);

  const name = firstName[ranNum1] + lastName[ranNum2];
  return name;
}
